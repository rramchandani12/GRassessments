/**
 * Uploads a file to the signed-in user's Google Drive.
 */
function uploadFile() {
  var file = document.getElementById('file').files[0];
  var metadata = {
    'name': file.name,
    'parents': ['0B1JEDjfcEEvta3RzVjZMNS03ZWM'] // Replace <FOLDER_ID> with the ID of the folder you want to upload to.
  };
  var accessToken = gapi.auth.getToken().access_token;
  var form = new FormData();
  form.append('metadata', new Blob([JSON.stringify(metadata)], {type: 'application/json'}));
  form.append('file', file);

  fetch('https://www.googleapis.com/upload/drive/v3/files?uploadType=multipart&fields=id', {
    method: 'POST',
    headers: new Headers({'Authorization': 'Bearer ' + accessToken}),
    body: form,
  }).then((res) => {
    return res.json();
  }).then(function(val) {
    console.log('File Id: ' + val.id);
    alert('File uploaded successfully to Google Drive!');
  });
}
